package Java_Based;

public class Arrays {
	public static void main(String[] args) {
		String name[] = { "Anna", "Kay", "Kellie", "Anitha", "Chihchung", "Sam", "Karsan" };
		System.out.println("Names are present in Arrays are : ");
		for (String nameData : name) {
			System.out.print(nameData + ", ");
		}
	}
}
